행정 = {"행정","세무","전산","사회복지","사서","속기","방호"}
def 군(s): return "행정직군" if s in 행정 else "기술직군"
def comp(sb,js):
    if not js or not sb: return ""
    v=math.floor(js/sb*10+0.5)/10
    return f"{v:.1f}:1"
def norm대상(t):
    t=t.strip()
    return {"장애":"장애인","저소득":"저소득층"}.get(t,t)
def save(region,src,rows,exp_sb,exp_js,flags=None):
    tsb=sum(int(r[11]) for r in rows); tjs=sum(int(r[12]) for r in rows)
    print(f"[{region}] 행수:{len(rows)} 선발:{tsb} 접수:{tjs}",end="  ")
    assert tsb==exp_sb and tjs==exp_js, f"불일치 {tsb}/{tjs} 기대 {exp_sb}/{exp_js}"
    print("검산 통과 ✅")
    io.open(f"{BASE}/_work/out/9급_{region}.json","w",encoding="utf-8").write(json.dumps(
     {"region":region,"category":"9급접수","source":src,"rows":rows,
      "checks":{"sum_선발":tsb,"sum_접수":tjs,"doc_총계":f"{exp_sb}/{exp_js}"},"flags":flags or[]},ensure_ascii=False,indent=1))

